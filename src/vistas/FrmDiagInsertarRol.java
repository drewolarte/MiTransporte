    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        RolDB objRolDB = new RolDB();
        Rol objRol = new Rol();

        objRol.setRol(this.txtRol.getText());

        objRol = (Rol) objRolDB.insertar(objRol);

        if (objRol.getId() > 0) {
            JOptionPane.showMessageDialog(this, "El rol se insertó con éxito.");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error: No se pudo insertar el rol.");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

